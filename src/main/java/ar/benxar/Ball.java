package ar.benxar;


public class Ball
{
    Integer number;
    Integer weight;

    public Ball(Integer n, Integer w) {
        this.number = n;
        this.weight = w;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }

    public Integer getWeight() {
        return weight;
    }

    public void setWeight(Integer weight) {
        this.weight = weight;
    }

} 